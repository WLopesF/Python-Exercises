age = int(input("Digite a sua idade"))

if (age == 18):
    print("Você recentemente se tornou maior de idade! Agora você responde por si mesmo(a)")

if (age < 18):
    print("Você é menor de idade! Seus pais ainda respondem por você.")

if (age > 18):
    print("Você é maior de idade há um tempo! Continue tomando cuidado.")